<p id="fn7"><b>7</b>: Traagisena esimerkkinä tästä mainittakoon Turun Ylioppilasteatterin kevään 2018 produktio ”Godota
odottaessa” joka jouduttiin perumaan teoksen käsikirjoittaja Samuel Beckettin perikunnan määrättyä, ettei
esityksessä saa olla naisnäyttelijöitä (<a href="#S-TYT2018">Turun Ylioppilasteatteri 2018</a>).</p>

