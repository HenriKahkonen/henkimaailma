<p>
Mitä sellaista Vahvaselkä ja Terras ovat sitten kuulleet ”Hello”:ssa, joka on tuotu tai muutettu
”Bonjour”-numeroihin? Vahvaselkä itse kertoo kappalevalinnan olleen enemmän vain tämän
henkilökohtaisen Book of Mormon -fanituksen tulosta (A6), mutta alkuperäiskappaleen
assosiatiivisia merkityksiä voi nähdä ”valuneen yli” myös Bonjouriin. Bonjourin lauluosuudet
on jaettu ”Hello”:n tavoin laulavan ensemblen sooloihin, tällä kertaa esitellen ranskalaisia
turistikohteita ja kulttuurielämyksiä mormonien lähetystyön suorittamisen sijasta. ”Hello”:n
laskeva bassolinja ja neljäsosia korostava kevyt marssirytmi tuo tekstiin määrätietoisen,
eteenpäin kulkevan tunnelman, joka alleviivaa lähetystyön periksiantamattomuutta
vastahakoistenkin kuuntelijoiden edessä. Optimistisen sävelmän yhdistäminen mormoneihin
liitettyyn tungettelevuuteen ja ärsyttävyyteen liittää ”Hello”:ssa laulaviin
lähetystyöntekijöihin eräänlaista autuasta itsetietoisuuden puutetta, jota pahaa-aavistamattomat
ihmiset joutuvat sietämään ovelleen saapuvia mormoneja kohdatessaan.    
</p>
